# to measure the running time there are two mwthods
#   1. experimental method-   **CONSUME A LOT OF TIME**
#       - implement the algorithm in a language.(depends on the softwware and hardware). 
#       - run it on different inputs.(limited number of inputs).
#       - record the exact running time.(not feasible for algorithm that take long time for execution).
#       - there are certain limitations for this method.


#   2. analytical method - asymptotic analysis
#       - analyse running time based on input size.
#       - independent of the hardware and software.
#       - consider all possible inputs.
#               running time ---> size of inputs(n).
#               size of inputs small ---> less running time. for example - sorting algo.
#               size of inputs more ---> more running time. for example - sorting algo.